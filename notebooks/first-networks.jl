function distance_matrix(graph)
	n = nv(graph) # number of vertices
	
	distance_matrix = zeros(Int, n, n)
	
	for (i, node) in enumerate(vertices(graph))
		distance_matrix[i, :] .= gdistances(graph, node)
	end
	
	distance_matrix
end

